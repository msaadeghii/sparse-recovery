function [ w_n ] = lmbwm1( x )
x = (x<-exp(-1)).*(-exp(-1)) + (x>=-exp(-1)).*x;
x2 = x.^2;
x3 = x.^3;
x4 = x.^4;
x5 = x.^5;
w_n = -7.81417672390744 + 253.88810188892484 * x + 657.9493176902304 * x2;
w_n = w_n ./ (1 -60.43958713690808 * x + 99.9856708310761 * x2 + 682.6073999909428 * x3 + 962.1784396969866 * x4 + 1477.9341280760887 * x5);

for i = 1:2
    z_n = log(x./w_n) - w_n;
    q_n = 2 * (1+w_n) .* (1+w_n+2/3 * z_n);
    e_n = (z_n ./ (1+w_n)) .* (q_n-z_n) ./ (q_n - 2*z_n);

    w_n = w_n .* (1 + e_n);
end
w_n = (x>-exp(-1)).* w_n;
w_n(find(x == 0)) = 0;
end
